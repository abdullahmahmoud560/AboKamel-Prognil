    // دالة لإضافة البراندات للداتابيز بدون تكرار
    public static async Task SeedBrandsAsync(CapsulaDbContext context)
    {
        // جلب أسماء البراندات الموجودة بالفعل في الداتابيز
        var existingBrands = await context.Brands.Select(b => b.Name).ToListAsync();
        // جلب قائمة البراندات الفريدة
        var uniqueBrands = GetUniqueBrandNames();
        // تصفية البراندات الجديدة فقط (اللي مش موجودة بالفعل)
        var newBrands = uniqueBrands.Where(name => !existingBrands.Contains(name)).ToList();

        // إضافة البراندات الجديدة للداتابيز
        if (newBrands.Any())
        {
            foreach (var brandName in newBrands)
            {
                var brand = new Brand
                {
                    Name = brandName,
                    Slug = brandName.Replace(" ", "-").ToLower(),
                    ImagePath = ""
                };
                // إضافة البراند للـ Context
                context.Brands.Add(brand);
            }
            // حفظ التغييرات في الداتابيز
            await context.SaveChangesAsync();
        }
    }

